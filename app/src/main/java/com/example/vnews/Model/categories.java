package com.example.vnews.Model;

public class categories {
    private String id;
    private String name;

    public categories() {
    }

    public categories(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}